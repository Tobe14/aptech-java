public class SwitchPractice {
    static void main() {
        String name = "Osi";
        switch (name) {
            case "Ayo":
                System.out.println("Denied");
                break;
            case "Tobe" :
                System.out.println("Defs Allowed");
                break;
            default:
                System.out.println("You may be allowed");
        }
    }
}
